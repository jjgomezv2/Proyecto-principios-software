    public void aumentar_saldo(double monto){
            try {
                PreparedStatement ps = null;                
                //saldo=saldo+monto;
                ps = cn.prepareStatement("UPDATE usuarios SET saldo="+(saldo+monto)+" WHERE identificacion="+identificacion+";");
                ps.executeUpdate();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error al recopilar datos", "Error de DB", JOptionPane.ERROR_MESSAGE);
            }
    }
    
    public void disminuir_saldo(double monto){
            try {
                PreparedStatement ps = null;                
                //saldo=saldo-monto;
                ps = cn.prepareStatement("UPDATE usuarios SET saldo="+(saldo-monto)+" WHERE identificacion="+identificacion+";");
                ps.executeUpdate();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error al recopilar datos", "Error de DB", JOptionPane.ERROR_MESSAGE);
            }
    }
    
