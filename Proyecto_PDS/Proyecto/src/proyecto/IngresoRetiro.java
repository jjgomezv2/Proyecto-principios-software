    private void aceptarIngRetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarIngRetActionPerformed
        // TODO add your handling code here:
        String opcion= (String) opcionIngRet.getSelectedItem();
        double valor = Double.parseDouble(dineroIngRet.getText());
        if (opcion.equals("Ingresar dinero")) {
            aumentar_saldo(valor);
            JOptionPane.showMessageDialog(null, "Se han ingresado exitosamente COP "+valor, "Confirmación", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
            Funcioprincipales principal = new Funcioprincipales();
            principal.setVisible(true);
        } else { if(opcion.equals("Retirar dinero")){
                if(valor<=saldo){
                    disminuir_saldo(valor);
                    JOptionPane.showMessageDialog(null, "Se han retirado exitosamente COP "+valor, "Confirmación", JOptionPane.INFORMATION_MESSAGE);
                    this.setVisible(false);
                    Funcioprincipales principal = new Funcioprincipales();
                    principal.setVisible(true);
                } else{
                    JOptionPane.showMessageDialog(null, "Saldo insuficiente", "Error Form", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_aceptarIngRetActionPerformed

